public class variables_in_Java {
    public static void main(String[] args){
        int age;
        age = 121;
        System.out.println(age);

        System.out.println("Давай: " + age + " раз закодим");

        float  num1 = 4.2463864f;
        System.out.println(num1);
    }
}

